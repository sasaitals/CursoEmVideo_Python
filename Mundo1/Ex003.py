#Crie um programa que leia dois números e mostre a soma entre eles.
num1 = int(input('Digite o primeiro número: '))
num2 = int(input('Digite o segundo número: '))

#2 formas de print
#1º
result = num1+num2
print(f'O resultado da soma dos números {num1} e {num2} é {result}!')

#2º
print(f'O resultado da soma dos números {num1} e {num2} é {num1 + num2}!')