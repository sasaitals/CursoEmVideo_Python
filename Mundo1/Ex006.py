#Crie um algoritmo que leia um numero e mostre o seu dobro, triplo e raiz quadrada.
num = int(input('Digite um número para saber o dobro, o triplo e sua raiz quadrada: '))

#1º
d = num * 2
t = num * 3
rq = num ** 0.5
print(f'Dobro: {d}\nTriplo: {t}\nRaiz Quadrada: {rq}')

#2º
print(f'Dobro: {num * 2}\nTriplo: {num * 3}\nRaiz Quadrada: {num ** 0.5}')